    <h2>whatis /n/r/j</h2>

	<p>
	    /neron/root/journaux est un gestionnaire de journal de bord à destination des membres d'équipage du Daedalus.
	</p>

	<p>
	    Ce gestionnaire vous permet de rédiger quotidiennement un rapport qui sera ajouté à la liste de vos rapports lors de votre séjour à bord du Daedalus. À la fin de votre voyage, ou si la mort doit survenir avant cela, vous pouvez mettre fin à ce journal et en ouvrir un nouveau pour une nouvelle aventure. NRJ conserve la liste de vos différents journaux sur son serveur.
	</p>

	<p>
	    Quand la partie est achevée pour tout le monde, vous avez la possibilité d'attribuer l'identifiant "theEnd" à votre journal. C'est là qu'apparait tout l'intérêt de NRJ : il vous est possible de parcourir la chronologie des événements avec l'imbrication des journaux des autres passagers. Vous pouvez découvrir ainsi ce qu'ils ont pensé derrière leurs belles paroles, ce qu'ils ont accompli dans le plus grand secret en jouant les innocents, vous pouvez découvrir les manigances des mushs contre les humains ou les plans des humains pour faire tomber les mushs.
	</p>

	<p>
	    Une partie achevée (dont au moins un journal a un theEnd attribué) peut être consultée depuis les archives publiques. Vous pouvez ainsi découvrir les aventures des équipages d'autres vaisseaux Daedalus.
	</p>
