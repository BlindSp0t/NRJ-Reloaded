    <h2>./neron faq</h2>

	<p>
	    Vous trouverez ici la liste des renseignements les plus basiques pour la bonne compréhension de NRJ.
	</p>

	<h3>Pourquoi dois-je utiliser mon pseudonyme Twinoïd pour m'inscrire ?</h3>
	<p>
	    Le pseudo Twinoïd sert de carte d'identité pour les utilisateurs de NRJ. Il permet de s'assurer en vérifiant les palmarès theEnd que les joueurs étaient bien ceux qu'ils prétendaient être, et ce à la bonne partie donnée.
	</p>

	<s><h3>Pourquoi y a t-il des restrictions à l'inscription ? pourquoi ne pas accepter tout le monde ?</h3></s>
	<p>
	    <s>Ces restrictions visent à réserver l'usage de NRJ à des joueurs ayant déjà une véritable expérience de jeu pour raconter leurs exploits. De plus, des personnes qui tricheraient sur leur rôle, leur theEnd ou encore les actions accomplies pourraient voir leur compte NRJ bloqué. La restriction à l'inscription évite l'arrivée de multi-comptes ayant pour but de contourner ces blocages.</s>
	    Dorénavant, tout le monde peut s'inscrire et commencer immédiatement à enregistrer ses aventures sous forme de journaux. La seule chose requise est un compte Twinoïd.
	</p>

	<h3>Je suis mush, je ne vais pas raconter mon histoire, je vais être grillé de suite !</h3>
	<p>
	    Les journaux sont strictement privés jusqu'à ce qu'ils soient achevés par l'utilisateur (fin de partie, mort, etc). De plus, seul l'ajout final du theEnd permet de lier les journaux entre eux, il n'est pas possible auparavant de trouver le journal d'un joueur participant à un voyage précis.
	</p>

	<h3>Puis-je écrire plus d'un rapport par jour ?</h3>
	<p>
	    Non, afin que la lecture reste digeste lors des regroupements de journaux d'une même aventure et de s'assurer de la bonne chronologie des événements. Il vous faut rédiger les faits de la journée en un seul rapport. Vous pouvez toujours écrire ce rapport en fin de journée ou le lendemain, il n'est pas obligatoire de l'écrire le jour même. Vous pouvez d'ailleurs sauter des jours sans problème.<br />
	    Cependant, tant que le jour n'est pas passé vous pouvez modifier le rapport d'aujourd'hui et ainsi le rédiger en plusieurs fois ou le corriger par la suite.
	</p>

	<h3>J'ai regardé un journal public il y a quelques jours, et en y retournant j'ai remarqué de nouveaux messages.</h3>
	<p>Les codes theEnd peuvent être entrés n'importe quand. Au moment où vous consultez un journal public, d'autres joueurs y ayant participé et rédigé sur NRJ n'ont peut-être pas encore précisé leur theEnd et le feront plus tard.</p>

	<h3>Le design est vraiment bien.</h3>
	<p>Les éléments graphiques de Mush s'inspirent de la science-fiction des années 70-80, avec les gros blasters laser, les talkies à l'ancienne et écrans monochromes (carte), le Pilgred qui est une lampe à plasma géante... Partant de là, je voulais faire de NRJ une interface d'ordi à l'ancienne pour rester dans le ton avec un monochrome vert, du texte pixélisé et un design minimaliste.<br>
	Cette nouvelle version du design du site qui correspond parfaitement à ce que je cherchais est l'œuvre d'un joueur de Mush qui préfère ne pas avoir son nom cité mais qui a fourni un formidable travail bénévole. Un grand bravo à lui, je ne le remercierai jamais assez.</p>
	<p class="dark">(merci Liezon)</p>    

	<h3>Mais, je reconnais ce site ?!</h3>
	<p>En effet, le site n'est pas nouveau. Il était développé à la base par Meowcate, dans le but de fournir à tous un outil pour enregistrer ses journaux. Malheureusement, les évènements ont fait que Meowcate n'a plus le temps de s'occuper de NRJ. Les membres de son casting ont donc décidé unanimement de reprendre le concept du site tout en l'adaptant un petit peu
	afin de permettre à tout un chacun de tenir son propre journal (les inscriptions étant auparavant validées manuellement par Meowcate, il n'était plus possible de s'inscrire). Les inscriptions se font dorénavant de manière automatique, et tout ce dont vous avez besoin est un compte Twinoïd.</p>        
